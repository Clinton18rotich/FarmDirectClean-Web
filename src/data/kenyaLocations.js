export const getCounty = (name) => KENYA_COUNTIES.find(c => c.name === name);
export const getSubCounties = (countyName) => getCounty(countyName)?.subCounties || [];
export const getWards = (countyName, subCountyName) => {
  const sc = getSubCounties(countyName).find(s => s.name === subCountyName);
  return sc?.wards || [];
};
export const getVillages = (countyName, subCountyName, wardName) => {
  const wards = getWards(countyName, subCountyName);
  return wards.find(w => w.name === wardName)?.villages || [];
};
